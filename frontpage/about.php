                            <p>HSTU is relatively new as a university having been established in 1999. However, as an academic institution its history dates back to 1979 when it started its journey as an Agricultural Extension Training Institute (AETI) created to offer three year diploma course in Agriculture. In a matter of a decade it was elevated to degree-giving institution in 1988 and named as Hajee Mohammad Danesh Agricultural College. Ultimately based on the performance of Agricultural College but more importantly to cater to the crying need for highly skilled manpower in Science and Technology and for promotion of research in these vital areas Prime Minister of that time Sheikh Hasina declared to upgrade Hajee Mohammad Danesh Agriculture College to Science and Technology University on February 1999 in a public meeting held at Gor-E-Shahid Maidan of Dinajpur. On 6th September the property liabilities was handed over of former Agriculture College from Ministry of Agriculture to Ministry of Education.</p>
